export type UiTranslationKey = keyof typeof uiTranslations.en;

export const getUiText = (key: UiTranslationKey, lang: Language = 'en'): string => {
  const fallbackChain: Language[] =
    lang === 'en'
      ? ['en']
      : lang === 'hi'
      ? ['hi', 'en']
      : ['hi', 'en'];

  for (const candidate of fallbackChain) {
    const value = uiTranslations[candidate][key];
    if (value) return value;
  }

  return uiTranslations.en[key] || '';
};

export const formatUiText = (
  key: UiTranslationKey,
  lang: Language = 'en',
  params: Record<string, string | number> = {}
): string => {
  let template = getUiText(key, lang);
  Object.entries(params).forEach(([paramKey, paramValue]) => {
    template = template.replaceAll(`{${paramKey}}`, String(paramValue));
  });
  return template;
};

